# 변수값 교환 1
val1 = "파이썬을 연습해서 어디다 쓰냐고요? "
val2 = "나중에 게임 만들겁니다. 하하! "

print(val1 + val2)

temp = val1
val1 = val2
val2 = temp

print(val1 + val2)

